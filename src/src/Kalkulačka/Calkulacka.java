
public class Calkulacka {

	public int scitej(int x, int y) {
		int z;
		z = x + y;
		return z;
	}

	public int odecitej(int x, int y) {
		int z;
		z = x - y;
		return z;
	}

	public int del(int x, int y) {
		int z;
		z = x / y;
		return z;
	}

	public int vynasob(int x, int y) {
		int z;
		z = x * y;
		return z;
	}
}